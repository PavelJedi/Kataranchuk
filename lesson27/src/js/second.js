const keyNumber = prompt(`enter number`)

switch (keyNumber){
    case '1':
        console.log('!');
        break;
    case '2':
        console.log('@')
        break;
    case '3':
        console.log('#')
        break;
    case '4':
        console.log('$')
        break;
    case '5':
        console.log('%')
        break;
    case '6':
        console.log('^')
        break;
    case '7':
        console.log('&')
        break;
    case '8':
        console.log('*')
        break;
    case '9':
        console.log('(')
        break;
    default:
        console.log('unknown number')
};